    <p>Did you know that Sushi has been consumed in Japan since its introduction from Southeast Asia during the 8th century C.E.? Back in those days, sushi was more a method of preserving fish in lacto-fermented rice. There the fish could keep for months at a time, and the fermented rice would be discarded when the fish was eaten. Between the 17th and 19th century, lacto-fermentation was replaced with vinegar to sour the rice.</p>

    <p>Today's modern form of sushi was invented as an early form of "fast food" by Hanaya Yohei (1799–1858), a resident of what is now known as Tokyo. People in Tokyo were living in haste even way back then— the nigiri sushi invented by Hanaya was not fermented and could be eaten using the fingers or chopsticks.</p>

    <p>Sushi Town takes their inspiration from Hanaya, and infuses it with a contemporary spin that is sure to excite!</p>
    
